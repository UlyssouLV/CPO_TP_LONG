
package Armes;

public class Arme {
   
    private int level;
    private String nom;
    
    // Constructeur
    public Arme(int lvl, String name) {
        this.level=lvl;
        this.nom=name; 
    } // Fin Constructeur

    // Getter Level
    public int getLevel() {
        return level;
    }
    //Getter Nom
    public String getNom() {
        return nom;
    }
  
    
    
    
    
    
}
